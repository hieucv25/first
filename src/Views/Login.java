    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            String user = tfUser.getText();
            String pass = pfPass.getText();
            if (checkLoginNV(user, pass)) {
                JOptionPane.showMessageDialog(this, "Login successfully!");
                FormNV fnv = new FormNV(this, true);
                fnv.setVisible(true);
                this.dispose();
            } else if (checkLoginKH(user, pass)) {
                JOptionPane.showMessageDialog(this, "Login successfully!");
                FormKH fkh = new FormKH(this, true, user);
                fkh.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Username or Pass incorrect!");

            }
        } else {
            JOptionPane.showMessageDialog(this, "Username or Pass not entered yet!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

