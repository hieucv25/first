    public boolean validateForm() {
        if (tfUser.getText().isEmpty() && pfPass.getText().isEmpty()) {
            return false;
        }
        return true;
    }

    public boolean checkLoginNV(String ma, String matKhau) {
        for (NhanVien nv : listNV) {
            if (ma.equalsIgnoreCase(nv.getMa()) && matKhau.equalsIgnoreCase(nv.getMatKhau())) {
                return true;
            }
        }
        return false;
    }

    public boolean checkLoginKH(String ma, String matKhau) {
        for (KhachHang kh : listKh) {
            if (ma.equalsIgnoreCase(kh.getMa()) && matKhau.equalsIgnoreCase(kh.getMatKhau())) {
                return true;
            }
        }
        return false;
    }

